package com.test;

public class ForDemo {
    public static void main(String[] args) {
        System.out.println("Hello, World");
        System.out.println("Hello, World");
        System.out.println("Hello, World");
        System.out.println("Hello, World");
        System.out.println("Hello, World");
        System.out.println("---------");

        for (int i = 1; i <= 5; i++) { //int i = 1: initialize the for loop, i <= 5: for loop terminal condition
            //i++: for loop condition controller
            System.out.println("Hello, World");
        }
    }
}
